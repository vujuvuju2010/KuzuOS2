// Reverse bits in a byte to fix mirroring
static uint8_t reverse_bits(uint8_t b) {
    b = (b & 0xF0) >> 4 | (b & 0x0F) << 4;
    b = (b & 0xCC) >> 2 | (b & 0x33) << 2;
    b = (b & 0xAA) >> 1 | (b & 0x55) << 1;
    return b;
}

// Draw a single pixel at (x, y) with color
static void draw_pixel(int x, int y, uint32_t color) {
    if (x < 0 || x >= (int)fb_width || y < 0 || y >= (int)fb_height || !framebuffer)
        return;
        
    uint32_t* pixel = framebuffer + y * (fb_pitch / 4) + x;
    *pixel = color;
}

// Draw an 8x16 character at pixel position (x, y) - like Linux TTY
static void draw_char(int x, int y, char c, uint8_t fg_color, uint8_t bg_color) {
    if (!framebuffer) return;
    
    uint32_t fg = vga_colors[fg_color & 0x0F];
    uint32_t bg = vga_colors[bg_color & 0x0F];
    
    // Ensure we use valid ASCII characters only
    uint8_t char_index = (uint8_t)c;
    if (char_index > 127) char_index = '?'; // Replace non-ASCII with ?
    
    uint8_t* glyph = font_8x8[char_index];
    
    for (int row = 0; row < 8; row++) {
        uint8_t line = reverse_bits(glyph[row]); // FIXED: Now using reverse_bits!
        for (int col = 0; col < 8; col++) {
            // Read bits MSB first (standard font format)
            uint32_t color = (line & (0x80 >> col)) ? fg : bg;
            // Draw each pixel twice vertically to make 8x16 (Linux TTY size)
            draw_pixel(x + col, y + row * 2, color);
            draw_pixel(x + col, y + row * 2 + 1, color);
        }
    }
}

