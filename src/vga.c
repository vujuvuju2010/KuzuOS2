void putchar_color(char c, uint8_t color) {
    if (!framebuffer) return;
    
    int chars_per_line = fb_width / 8;    // 8 pixels wide per character (Linux TTY style)
    int max_lines = fb_height / 16;       // 16 pixels tall per character
    
    if (c == '\n') {
        cursor_x = 0;
        cursor_y++;
    } else if (c == '\r') {
        cursor_x = 0;
    } else if (c == '\b' || (unsigned char)c == 0x7F) { // backspace/delete visual erase
        if (cursor_x > 0) {
            cursor_x--;
            // draw a space at the current cell to clear
            uint8_t fg = color & 0x0F;
            uint8_t bg = (color >> 4) & 0x0F;
            draw_char(cursor_x * 8, cursor_y * 16, ' ', fg, bg);
        }
    } else if (c >= ' ') { // Printable character
        uint8_t fg = color & 0x0F;
        uint8_t bg = (color >> 4) & 0x0F;
        
        draw_char(cursor_x * 8, cursor_y * 16, c, fg, bg); // 8x16 character cells
        cursor_x++;
    }
    
    // Handle line wrapping
    if (cursor_x >= chars_per_line) {
        cursor_x = 0;
        cursor_y++;
    }
    
    // Handle scrolling
    if (cursor_y >= max_lines) {
        scroll();
        cursor_y = max_lines - 1;
    }
}

void putchar(char c) {
    putchar_color(c, VGA_COLOR_LIGHT_GREY);
}

void print(const char* str) {
    while (*str) {
        putchar(*str++);
    }
}

void print_color(const char* str, uint8_t color) {
    while (*str) {
        putchar_color(*str++, color);
    }
}

// Draw a bitmap image at position (x, y) with given width and height
// pixels should be RGBA32 format (BGRA in memory)
void vga_draw_bitmap(int x, int y, uint32_t width, uint32_t height, uint32_t* pixels) {
    if (!framebuffer || !pixels) return;
    
    // Calculate bounds for faster checking
    int x_start = (x < 0) ? 0 : x;
    int x_end = (x + (int)width > (int)fb_width) ? (int)fb_width : (x + (int)width);
    int y_start = (y < 0) ? 0 : y;
    int y_end = (y + (int)height > (int)fb_height) ? (int)fb_height : (y + (int)height);
    
    if (x_start >= x_end || y_start >= y_end) return;
    
    // Optimized pixel copying
    for (int py = y_start; py < y_end; py++) {
        int src_y = py - y;
        uint32_t* src_row = pixels + src_y * width;
        uint32_t* dst_row = framebuffer + py * (fb_pitch / 4);
        
        for (int px = x_start; px < x_end; px++) {
            int src_x = px - x;
            uint32_t pixel_color = src_row[src_x];
            
            // Check if pixel is transparent (alpha < 128)
            uint8_t alpha = (pixel_color >> 24) & 0xFF;
            if (alpha >= 128) {
                dst_row[px] = pixel_color;
            }
        }
    }
}
